    def _categorize(self, update: dict) -> str:
        """Determine which category an update belongs to."""
        text = f"{update.get('title', '')} {update.get('summary', '')} {update.get('company', '')}".lower()
        
        # Score each category
        scores = {}
        for category, keywords in self.CATEGORIES.items():
            score = sum(1 for kw in keywords if kw in text)
            if score > 0:
                scores[category] = score
        
        if not scores:
            return "📰 OTHER AI NEWS"
        
        # Return category with highest score
        return max(scores, key=scores.get)

    def _format_category(self, category: str, updates: list[dict]) -> str:
        """Format a single category section."""
        lines = [f"\n{category}", "=" * 40]
        
        for i, u in enumerate(updates, 1):
            impact = u.get("impact_level", "medium").upper()
            company = u.get("company", "Unknown")
            title = u.get("title", "No title")[:100]
            summary = u.get("summary", "")[:120]
            url = u.get("source_url", "")
            
            lines.append(f"\n{i}. {title}")
            lines.append(f"   Source: {company}")
            if summary:
                lines.append(f"   {summary}")
            lines.append(f"   Impact: {impact}")
            if url:
                lines.append(f"   Link: {url}")
        
        return "\n".join(lines)

    def format_categorized_messages(self, updates: list[dict]) -> list[str]:
        """Group updates by category and return list of message chunks.
        Each message is kept under 1000 chars to avoid Windows path length issues."""
        date_str = datetime.now(IST).strftime("%d %b %Y")
        
        if not updates:
            return [(
                f"🚀 AI Updates – {date_str}\n\n"
                "No significant AI updates in the last 24h.\n"
                "Stay tuned for tomorrow's briefing."
            )]
        
        # Categorize all updates
        categorized = {}
        for u in updates:
            cat = self._categorize(u)
            if cat not in categorized:
                categorized[cat] = []
            categorized[cat].append(u)
        
        # Sort categories by priority
        priority_order = [
            "💰 FINANCE & BUSINESS",
            "🏥 MEDICAL & HEALTHCARE", 
            "💻 CODING & DEVELOPMENT",
            "🤖 AI TECH & MODELS",
            "🔧 INDUSTRIAL & MANUFACTURING",
            "🚗 AUTONOMOUS & VEHICLES",
            "🔬 RESEARCH & SCIENCE",
            "⚡ HARDWARE & CHIPS",
            "📱 GENERAL TECH",
            "📰 OTHER AI NEWS"
        ]
        
        # Build messages (strict 900 char limit per message for safety)
        MAX_MSG_LEN = 900
        messages = []
        current_msg = f"🚀 AI Updates – {date_str}\n\n📊 Total: {len(updates)} updates\n"
        current_len = len(current_msg)
        
        for cat in priority_order:
            if cat not in categorized:
                continue
            
            cat_updates = categorized[cat]
            cat_header = f"\n{cat}\n" + "=" * 30 + "\n"
            
            # If category has many items, split across multiple messages
            for u in cat_updates:
                impact = u.get("impact_level", "medium").upper()
                company = u.get("company", "Unknown")
                title = u.get("title", "No title")[:80]
                url = u.get("source_url", "")[:60]  # Truncate URL
                
                entry = f"• {title}\n  {company} | {impact}\n"
                if url:
                    entry += f"  {url}\n"
                entry += "\n"
                
                # Check if adding this entry would exceed limit
                if current_len + len(entry) > MAX_MSG_LEN:
                    # Save current message and start new one
                    messages.append(current_msg)
                    current_msg = f"🚀 AI Updates – {date_str} (cont'd)\n{cat_header}{entry}"
                    current_len = len(current_msg)
                else:
                    # Add category header if first item in category
                    if cat_header not in current_msg:
                        current_msg += cat_header
                        current_len += len(cat_header)
                    current_msg += entry
                    current_len += len(entry)
        
        # Add final message
        if current_msg:
            messages.append(current_msg)
        
        return messages

    # ── Digest entry point ───────────────────────────────────────────────────

    def send_digest(self, updates: list[dict]) -> bool:
        """Format and send the daily digest in categorized sections.
        Sends ALL updates, grouped by category, split across multiple messages if needed.
        """
        if not updates:
            logger.warning("No updates to send")
            return True
        
        messages = self.format_categorized_messages(updates)
        logger.info("Sending %d message(s) with %d total updates", len(messages), len(updates))
        
        # Send each message
        all_sent = True
        for i, msg in enumerate(messages, 1):
            logger.info("Sending message %d/%d (%d chars)", i, len(messages), len(msg))
            print(f"\n📤 Sending part {i}/{len(messages)}...")
            
            success = self.send(msg)
            if not success:
                logger.error("Failed to send message %d", i)
                all_sent = False
                break
            
            # Small delay between messages
            if i < len(messages):
                time.sleep(5)
        
        # Mark all as sent if successful
        if all_sent:
            db = get_db()
            update_ids = [u["id"] for u in updates]
            db.mark_digest_sent(update_ids)
            logger.info("Marked %d updates as digest_sent", len(update_ids))
            print(f"\n✅ Sent {len(messages)} message(s) with {len(updates)} updates")
        
        return all_sent

    def send_pdf_digest(self, updates: list[dict]) -> bool:
        """Generate PDF report and send via email.
        Creates a professional PDF with categorized sections, tech launches prioritized.
        """
        if not updates:
            logger.warning("No updates to generate PDF")
            return True
        
        try:
            # Import here to avoid circular imports
            from reporting.pdf_generator import generate_pdf_report
            
            print(f"\n📄 Generating PDF report for {len(updates)} updates...")
            pdf_path = generate_pdf_report(updates)
            
            if not pdf_path or not os.path.exists(pdf_path):
                logger.error("PDF generation failed")
                return False
            
            print(f"✅ PDF generated: {pdf_path}")
            
            # Create summary message for WhatsApp
            date_str = datetime.now(IST).strftime("%d %b %Y")
            summary_msg = (
                f"🚀 AI Updates – {date_str}\n\n"
                f"📄 Your comprehensive AI digest is ready!\n\n"
                f"📊 Total updates: {len(updates)}\n"
                f"📑 Full PDF report sent to your email\n\n"
                f"The report includes:\n"
                f"• Tech launches prioritized\n"
                f"• Industry-wise categorization\n"
                f"• Impact levels & summaries\n"
                f"• Source links for details\n\n"
                f"Check your email ({settings.EMAIL_RECIPIENT}) for the PDF attachment."
            )
            
            # Send WhatsApp notification about PDF
            whatsapp_sent = False
            if settings.WHATSAPP_DIRECT_ENABLED:
                print("📱 Sending WhatsApp notification...")
                whatsapp_sent = self.send(summary_msg)
            
            # Send email with PDF attachment
            email_sent = False
            if settings.EMAIL_ENABLED:
                print("📧 Sending email with PDF attachment...")
                email_body = (
                    f"Your AI Update Digest for {date_str} is attached.\n\n"
                    f"Total updates analyzed: {len(updates)}\n\n"
                    f"The PDF includes categorized sections with tech launches prioritized, "
                    f"impact assessments, and source links for deeper reading."
                )
                email_sent = self._send_email(email_body, pdf_path)
            
            # Mark as sent if either succeeded
            if whatsapp_sent or email_sent:
                db = get_db()
                update_ids = [u["id"] for u in updates]
                db.mark_digest_sent(update_ids)
                logger.info("Marked %d updates as digest_sent", len(update_ids))
                print(f"\n✅ PDF digest delivered! WhatsApp: {whatsapp_sent}, Email: {email_sent}")
                return True
            else:
                logger.error("Both WhatsApp and email failed")
                return False
                
        except Exception as e:
            logger.error(f"PDF digest failed: {e}", exc_info=True)
            return False

    def close(self):
        self.client.close()

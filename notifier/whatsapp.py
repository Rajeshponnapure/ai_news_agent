import httpx
import pywhatkit

from config.settings import settings
from database.db import get_db

logger = logging.getLogger(__name__)

IST = timezone(timedelta(hours=5, minutes=30))


class WhatsAppNotifier:
    """Sends WhatsApp messages via direct WhatsApp (pywhatkit), Cloud API, or email fallback."""

    def __init__(self):
        self.max_retries = settings.MAX_RETRIES
        self.retry_delay = settings.RETRY_DELAY_SECONDS
        self.client = httpx.Client(timeout=30.0)

    # ── WhatsApp Direct (pywhatkit) ──────────────────────────────────────────

    def _send_whatsapp_direct(self, message: str) -> bool:
        """Send via pywhatkit — opens WhatsApp Web and sends to your number.
        Requires WhatsApp Web to be logged in on the machine (chrome/browser).
        """
        phone = settings.WHATSAPP_DIRECT_PHONE
        if not phone:
            logger.warning("WHATSAPP_DIRECT_PHONE not set, skipping direct WhatsApp")
            return False

        try:
            # Send now (wait_time=35 seconds gives WhatsApp Web time to fully load)
            logger.info("Opening WhatsApp Web... please wait 35 seconds for it to load")
            pywhatkit.sendwhatmsg_instantly(
                phone_no=phone,
                message=message,
                wait_time=35,
                tab_close=False,  # Don't auto-close so user can see it was sent
                close_time=0,
            )
            logger.info("WhatsApp direct message sent to %s", phone)
            return True
        except Exception as e:
            logger.error("WhatsApp direct send failed: %s", e)
            return False

    # ── WhatsApp Cloud API (optional, if you have a Meta token) ──────────────

    def _send_cloud_api(self, message: str) -> bool:
        """Send via WhatsApp Cloud API (requires Meta Business token)."""
        if not settings.WHATSAPP_TOKEN or not settings.WHATSAPP_PHONE_NUMBER_ID:
            logger.warning("WhatsApp Cloud API credentials not set, skipping")
            return False

        url = f"{settings.WHATSAPP_API_URL}/{settings.WHATSAPP_PHONE_NUMBER_ID}/messages"
        headers = {
            "Authorization": f"Bearer {settings.WHATSAPP_TOKEN}",
            "Content-Type": "application/json",
        }
        payload = {
            "messaging_product": "whatsapp",
            "to": settings.WHATSAPP_RECIPIENT_WA_ID,
            "type": "text",
            "text": {"body": message},
        }
        try:
            resp = self.client.post(url, json=payload, headers=headers)
            resp.raise_for_status()
            logger.info("WhatsApp Cloud API message sent successfully")
            return True
        except Exception as e:
            logger.error("WhatsApp Cloud API send failed: %s", e)
            return False

    # ── Email Fallback (SMTP) ────────────────────────────────────────────────

    def _send_email(self, message: str, pdf_path: str = None) -> bool:
        """Send via email (SMTP). Used as fallback when WhatsApp fails.
        Can optionally attach a PDF report."""
        if not settings.EMAIL_ENABLED:
            logger.info("Email fallback is disabled")
            return False
        if not settings.EMAIL_SENDER or not settings.EMAIL_PASSWORD or not settings.EMAIL_RECIPIENT:
            logger.warning("Email credentials not fully set, skipping email")
            return False

        date_str = datetime.now(IST).strftime("%d %b %Y")
        subject = f"🚀 AI Updates – {date_str}"

        msg = MIMEMultipart("mixed")
        msg["From"] = settings.EMAIL_SENDER
        msg["To"] = settings.EMAIL_RECIPIENT
        msg["Subject"] = subject

        # Body part
        body_part = MIMEMultipart("alternative")
        
        # HTML body if PDF attached
        if pdf_path:
            html_body = f"""
            <html>
            <body style="font-family: Arial, sans-serif; line-height: 1.6; color: #333;">
                <h2 style="color: #2980b9;">🚀 AI Update Digest – {date_str}</h2>
                <p>Your comprehensive AI intelligence briefing is attached as a PDF.</p>
                <p>The report includes:</p>
                <ul>
                    <li>Tech launches prioritized</li>
                    <li>Categorized by industry (Finance, Medical, Coding, AI Tech, etc.)</li>
                    <li>Impact levels and brief explanations</li>
                    <li>Source links for deeper reading</li>
                </ul>
                <p style="color: #7f8c8d; font-size: 12px;">
                    Generated by AI Agent | Sources: NewsAPI, GitHub, RSS, Tech Blogs
                </p>
            </body>
            </html>
            """
            body_part.attach(MIMEText(html_body, "html"))
            body_part.attach(MIMEText(message, "plain"))
        else:
            body_part.attach(MIMEText(message, "plain"))
        
        msg.attach(body_part)

        # Attach PDF if provided
        if pdf_path and os.path.exists(pdf_path):
            try:
                with open(pdf_path, "rb") as f:
                    pdf_attachment = MIMEBase("application", "octet-stream")
                    pdf_attachment.set_payload(f.read())
                
                encoders.encode_base64(pdf_attachment)
                pdf_filename = os.path.basename(pdf_path)
                pdf_attachment.add_header(
                    "Content-Disposition",
                    f"attachment; filename=\"{pdf_filename}\""
                )
                msg.attach(pdf_attachment)
                logger.info(f"PDF attached: {pdf_filename}")
            except Exception as e:
                logger.error(f"Failed to attach PDF: {e}")

        try:
            if settings.EMAIL_SMTP_PORT == 465:
                server = smtplib.SMTP_SSL(settings.EMAIL_SMTP_HOST, settings.EMAIL_SMTP_PORT)
            else:
                server = smtplib.SMTP(settings.EMAIL_SMTP_HOST, settings.EMAIL_SMTP_PORT)
                server.starttls()

            server.login(settings.EMAIL_SENDER, settings.EMAIL_PASSWORD)
            server.sendmail(settings.EMAIL_SENDER, settings.EMAIL_RECIPIENT, msg.as_string())
            server.quit()
            logger.info("Email sent successfully to %s", settings.EMAIL_RECIPIENT)
            return True
        except Exception as e:
            logger.error("Email send failed: %s", e)
            return False

    # ── Orchestration: try WhatsApp first, then email fallback ───────────────

    def send(self, message: str) -> bool:
        """Try WhatsApp (direct then Cloud API), then fall back to email.
        Each method gets up to max_retries attempts before moving to the next.
        """
        # 1. Try WhatsApp Direct (pywhatkit)
        if settings.WHATSAPP_DIRECT_ENABLED:
            for attempt in range(1, self.max_retries + 1):
                logger.info("WhatsApp Direct attempt %d/%d", attempt, self.max_retries)
                if self._send_whatsapp_direct(message):
                    return True
                if attempt < self.max_retries:
                    time.sleep(self.retry_delay)

        # 2. Try WhatsApp Cloud API (if configured)
        if settings.WHATSAPP_TOKEN:
            for attempt in range(1, self.max_retries + 1):
                logger.info("WhatsApp Cloud API attempt %d/%d", attempt, self.max_retries)
                if self._send_cloud_api(message):
                    return True
                if attempt < self.max_retries:
                    time.sleep(self.retry_delay)

        # 3. Email fallback
        if settings.EMAIL_ENABLED:
            logger.info("WhatsApp failed — falling back to email")
            for attempt in range(1, self.max_retries + 1):
                logger.info("Email attempt %d/%d", attempt, self.max_retries)
                if self._send_email(message):
                    return True
                if attempt < self.max_retries:
                    time.sleep(self.retry_delay)

        logger.error("All notification methods failed after retries")
        return False

